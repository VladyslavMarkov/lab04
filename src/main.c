#include <stdio.h>

int main()
{
    //Сторони ромба
    int a,b,c,d;
    
    a = 10;
    b = 10;
    c = 10;
    d = 10;
    
    //Перевіряємо рівні сторони ромба чи ні
    if(a == b && a == c && a == d)
    {
      //Якщо сторони рівні, то завершуємо роботу програми зі значенням 1
      return 1;
    }
               
    return 0;
}
